#ifndef __trace__constants__
#define __trace__constants__

const long NANO_SECOND  = 1;
const long MICRO_SECOND = 1000 * NANO_SECOND;
const long MILLI_SECOND = 1000 * MICRO_SECOND;
const long SECOND       = 1000 * MILLI_SECOND;
const long MINUTE       = 60 * SECOND;
const long HOUR         = 60 * MINUTE;
const long DAY          = 24 * HOUR;
const long WEEK         = 7 * DAY;

#endif /* defined(__trace__constants__) */
